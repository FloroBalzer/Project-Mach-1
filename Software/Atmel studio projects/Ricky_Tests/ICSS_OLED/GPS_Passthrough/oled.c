void oled_DrawBitmap(unsigned char *bitmaparray,int pages)
{
  char localAddressMode = addressingMode;
  uint8_t display_char;
  uint8_t shift_in_bit, shift_out_bit;
  uint8_t page, block,column, bit;
  
  if(addressingMode != HORIZONTAL_MODE)
   
  {
      //Bitmap is drawn in horizontal mode
      oled_SetHorizontalMode();
	 
	  
  }

  for(page=0; page < pages; page++)
  {
	   oled_SetTextXY(page , 0);
	   for(block=0; block < 16; block++){
		   shift_in_bit = 1;
		   for(column=0; column < 8; column++){
			   display_char = 0x00;			  
			   shift_out_bit = 0x01;
			   for(bit=0; bit < 8; bit++){
				   if((bitmaparray[((page * 128) + (bit * 16) + block)] & shift_in_bit) != 0) display_char |= shift_out_bit;
				   
				   shift_out_bit <<= 1;
			   }
			   oled_SendData(display_char);
			    shift_in_bit <<= 1;
		   }
		  
		   
	   }
	  
  }

  if(localAddressMode == PAGE_MODE)
  {
     //If pageMode was used earlier, restore it.
     oled_SetPageMode();
  }
  
}

