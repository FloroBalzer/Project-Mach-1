// Initialisation string for SSD1306 OLED display
uint8_t initbuf[]={0x00,0xae,0xa8,0x3f,0xd3,0x00,0x40,0xa1,0xc8,
	0xda,0x12,0x81,0xff,0xa4,0xa6,0xd5,0x80,0x8d,0x14,
0xaf,0x20,0x02};


static unsigned char addressingMode;
struct _i2c_m_msg msg;


void oled_Init(void)

{
    // Send Initialsation string
	msg.addr   = SeeedOLED_Address;
    msg.len    = sizeof(initbuf);
    msg.flags  = I2C_M_STOP;
    msg.buffer = &initbuf[0];
	i2c_m_sync_transfer(&I2C_1, &msg);
   
	
	delay_ms(100);
	oled_SendCommand(SeeedOLED_Display_Off_Cmd);     //display off
    delay_ms(5);
    oled_SendCommand(SeeedOLED_Display_On_Cmd);  //display on
    delay_ms(5);
    oled_SendCommand(SeeedOLED_Normal_Display_Cmd);  //Set Normal Display (default)
}

void oled_SendCommand(unsigned char command)
{
	uint8_t command_bytes[2];
	command_bytes[0] = 0x00;
	command_bytes[1] = command;
	msg.len = 2;
	msg.buffer = &command_bytes[0];
	i2c_m_sync_transfer(&I2C_1, &msg);
}

void oled_Send_2_Command(unsigned char command1, unsigned char command2)
{
	uint8_t command_bytes[3];
	command_bytes[0] = 0x00;
	command_bytes[1] = command1;
	command_bytes[2] = command2;
	msg.len = 3;
	msg.buffer = &command_bytes[0];
	i2c_m_sync_transfer(&I2C_1, &msg);
}

void oled_SetBrightness(unsigned char Brightness)
{
   oled_SendCommand(SeeedOLED_Set_Brightness_Cmd);
   oled_SendCommand(Brightness);
}

void oled_SetHorizontalMode()
{
    addressingMode = HORIZONTAL_MODE;
	
    oled_Send_2_Command(0x20, 0x00);     //set horizontal addressing mode
}

void oled_SetPageMode()
{
    addressingMode = PAGE_MODE;
    oled_Send_2_Command(0x20, 0x02);     //set page addressing mode
}


void oled_SetTextXY(unsigned char Row, unsigned char Column)
{
    oled_SendCommand(0xB0 + Row);            //set page address
    oled_SendCommand(0x00 + (8*Column & 0x0F));  //set column lower address
    oled_SendCommand(0x10 + ((8*Column>>4)&0x0F));   //set column higher address
}


void oled_ClearDisplay()
{
  unsigned char i,j;
  oled_SendCommand(SeeedOLED_Display_Off_Cmd);   //display off
  for(j=0;j<8;j++)
  {
    oled_SetTextXY(j,0);
    {
      for(i=0;i<16;i++)  //clear all columns
      {
        oled_PutChar(' ');
      }
    }
  }
  oled_SendCommand(SeeedOLED_Display_On_Cmd);    //display on
  oled_SetTextXY(0,0);
}

