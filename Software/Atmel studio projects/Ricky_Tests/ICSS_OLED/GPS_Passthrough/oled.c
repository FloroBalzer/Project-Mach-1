void oled_PutChar(unsigned char C)
{
    if(C < 32 || C > 127) //Ignore non-printable ASCII characters. This can be modified for multilingual font.
    {
    C=' '; //Space
    }
    unsigned char i=0;
    for(i=0;i<8;i++)
    {
       //read bytes from code memory
       oled_SendData(BasicFont[C-32][i]); //font array starts at 0, ASCII starts at 32. Hence the translation
    }
}

void oled_PutString(const char *String)
{
    unsigned char i=0;
    while(String[i])
    {
        oled_PutChar(String[i]);
        i++;
    }
}

unsigned char oled_PutNumber(long long_num)
{
  unsigned char char_buffer[10]="";
  unsigned char i = 0;
  unsigned char f = 0;

  if (long_num < 0)
  {
    f=1;
    oled_PutChar('-');
    long_num = -long_num;
  }
  else if (long_num == 0)
  {
    f=1;
    oled_PutChar('0');
    return f;
  }

  while (long_num > 0)
  {
    char_buffer[i++] = long_num % 10;
    long_num /= 10;
  }

  f=f+i;
  for(; i > 0; i--)
  {
    oled_PutChar('0'+ char_buffer[i - 1]);
  }
  return f;

}

unsigned char oled_PutFloatn(float floatNumber,unsigned char decimal)
{
  unsigned int temp=0;
  float decy=0.0;
  float rounding = 0.5;
  unsigned char f=0;
  if(floatNumber<0.0)
  {
    oled_PutString("-");
    floatNumber = -floatNumber;
    f +=1;
  }
  for (unsigned char i=0; i<decimal; ++i)
  {
    rounding /= 10.0;
  }
    floatNumber += rounding;
  
  temp = floatNumber;
  f += oled_PutNumber(temp);
  if(decimal>0)
  {
    oled_PutChar('.');
    f +=1;
 }
  decy = floatNumber-temp;//decimal part,
  for(unsigned char i=0;i<decimal;i++)//4
  {
    decy *=10;// for the next decimal
    temp = decy;//get the decimal
    oled_PutNumber(temp);
    decy -= temp;
  }
  f +=decimal;
  return f;
}
